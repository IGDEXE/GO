package main

// Importa a biblioteca
import( 
	"fmt"
)

// Funcao principal
func main() {
	nome := "Ivo" // String
	idade := 22 // Inteiro
	triste := true // Boleano
	fmt.Printf("Meu nome eh %v\n",nome) // %v\n mostra qualquer tipo de dado
	fmt.Printf("Minha idade %v\n",idade)
	fmt.Printf("Eu estou triste? %v\n",triste)
}