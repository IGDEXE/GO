// Define o pacote principal
package main

// Importa o pacote FMT
import "fmt"

// Chama a funcao principal
func main() {
	// Strings podem ser unidas com o operador +
	fmt.Println("go" + "lang")
	// Soma com inteiros
	fmt.Println("1+1 =", 1+1)
	// Divisao com ponto flutuante
	fmt.Println("7.0/3.0 =", 7.0/3.0)
	// Operador booleano E
	fmt.Println(true && false)
	// Operador booleano OU
	fmt.Println(true || false)
	// Operador booleano NAO
	fmt.Println(!true)
}
